package Modifiers;

public class gfinal_non_access_modifier_classA {

	public static void main(String[] args) {

		/*
		  	* final is Non-access modifier
		  	* final is used with variables, methods and classes
		  	 
		  	* You can only assign the value to the variable once
		  	 
		// Variables :
		   ---------  	
		int a = 5;
		
		a = 9;
		
		a = 11;
		
		a = 15; 
		
		System.out.println(a);    //Any no.of values are there to same variable but Latest value will be printed
	
		
		// If we keep non-access(final) modifier to one of the variables we get compiler error
		// You can only assign the value to the variable once
		// error is The final local variable a cannot be assigned. It must be blank and not using a compound assignment
		// If we remove the 5 in 1st step it take 9 as value and give error to 11 & 15
		final int a = 5;
		
		a = 9;
		
		a = 11;
		
		a = 15; 
		
		System.out.println(a);
		
		Classes :
		-------
		* if you assign "final" non-access modifier to classes these classes cannot be inherited by other classes
		
		Methods :
		-------
		* if you assign "final" non-access modifier to methods, "we cannot override the methods"
		
		
		
		
		*/
		
		
	}

}
