package constructors;

public class cintroduction {
	
	/*
	 	Constructors looks similar like methods
	 	
	 	constructors :
	 	------------
	 	public cintroduction()
	 	{
	 	
	 	}
	 	* constructors must have the name same as class name
	 	* No return type
	 	* automatically called during object creation
	 	
	 	methods() :
	 	---------
	 	public void startcar()
	 	{
	 	
	 	}
	 	* methods has their own names
	 	* Have return type, void is return type
	 	* specifically called
	 	 	
	 */
	
	public cintroduction()
 	{
		System.out.println(" Inside cintroduction constructor ");
 	}
	
	public void startcar()
 	{
		System.out.println(" Inside startcar method ");
 	}

}
